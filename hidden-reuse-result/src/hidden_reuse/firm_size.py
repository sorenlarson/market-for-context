"""Firm size when owned context can generate transferable learning.

The bilateral hidden-reuse game explains why one context-generating asset may
move inside a firm.  This module adds the missing scale margin: how many
homogeneous assets should be governed by the same firm?

For a firm owning ``n`` assets, incremental value relative to modular trade is

    V(n) = (
        n A n**(-zeta)
        - K
        + n L (n - 1) / (kappa + n - 1)
        - d n**rho
        - c n**(1 + eta)
    ).

``A`` is the per-asset advantage of internalization supplied by the bilateral
governance problem, and ``zeta >= 0`` governs how that advantage dilutes with
firm size: the per-asset advantage at size ``n`` is ``A n**(-zeta)``.  ``K`` is
a shared fixed AI-system cost, and ``L`` is the ceiling on transferable
cross-asset learning per asset. Integration execution cost ``d n**rho`` has
declining marginal cost when ``0 < rho < 1``: later integrations reuse the
acquirer's playbook, systems, diligence, and financing infrastructure. The
final term is the convex ongoing burden of coordination, bureaucracy,
correlated liability, and lost local adaptation.

With homogeneous assets, transferable utility, and free coalition formation,
potential firms maximize value per member.  When ``zeta = 0`` (the default)
the advantage term ``A`` is additive across sizes: it changes whether the best
firm creates positive surplus, but it drops out of the firm-size choice.  This
is the model's pledgeability-transferability separation, and it holds if and
only if the advantage is size-independent. Conditional size is determined by
shared fixed costs, transferable learning, declining marginal integration
cost, and increasing ongoing coordination cost. With ``zeta > 0`` the entry
and size margins interact: a larger ``A`` shifts the conditional optimum
toward smaller firms because the advantage is worth more per asset at small
scale.
"""

from dataclasses import dataclass, replace
from enum import Enum
from math import isfinite
from typing import Optional

from .model import (
    HiddenReusePrimitives,
    HiddenReuseRegime,
    current_benefit,
    solve_hidden_reuse,
)
from .pledgeability import PledgeabilityPrimitives, solve_with_pledgeability


class FirmSizeRegime(str, Enum):
    """Ownership structures generated by the homogeneous coalition game."""

    MODULAR = "modular_market"
    STANDALONE = "standalone_integration"
    ROLLUP = "finite_rollup"
    BOUNDARY = "maximum_feasible_scale"

    # Alias for callers that interpret max_firm_size as the total number of
    # assets in a finite industry.
    INDUSTRY_WIDE = BOUNDARY


@dataclass(frozen=True)
class FirmSizePrimitives:
    """Primitives of the homogeneous firm-size game.

    ``internalization_advantage`` is incremental private value per asset before
    the scale-specific terms in this model. It may be negative. The remaining
    cost and benefit parameters must be nonnegative.

    Total integration-execution cost is ``d n**rho``. Restricting
    ``integration_cost_elasticity`` to ``0 < rho <= 1`` gives weakly declining
    marginal execution cost and makes its per-asset burden weakly fall with
    size. Total ongoing organization cost is ``c n**(1 + eta)``. Restricting
    ``organization_cost_elasticity`` to at least one makes its per-asset burden
    convex. Together these restrictions preserve concavity of continuous
    per-asset surplus and give a single-peaked size problem.

    ``advantage_dilution_elasticity`` (zeta) makes the per-asset advantage
    size-dependent: at firm size ``n`` it is ``A n**(-zeta)``. The default
    ``0.0`` reproduces the additive baseline exactly. With ``zeta > 0``, the
    single-peakedness guarantee above no longer applies because the advantage
    term is convex in continuous size, so the continuous benchmark is
    restricted to ``zeta = 0``.

    ``max_firm_size`` is an industry or computational boundary, not an estimate
    of the number of real assets in a market.
    """

    internalization_advantage: float = 0.65
    shared_fixed_cost: float = 1.50
    cross_node_learning: float = 0.35
    learning_saturation: float = 4.00
    integration_cost_scale: float = 0.50
    integration_cost_elasticity: float = 0.65
    organization_cost_scale: float = 0.015
    organization_cost_elasticity: float = 1.40
    advantage_dilution_elasticity: float = 0.0
    max_firm_size: int = 60

    def validate(self) -> None:
        finite_values = {
            "internalization_advantage": self.internalization_advantage,
            "shared_fixed_cost": self.shared_fixed_cost,
            "cross_node_learning": self.cross_node_learning,
            "learning_saturation": self.learning_saturation,
            "integration_cost_scale": self.integration_cost_scale,
            "integration_cost_elasticity": self.integration_cost_elasticity,
            "organization_cost_scale": self.organization_cost_scale,
            "organization_cost_elasticity": self.organization_cost_elasticity,
            "advantage_dilution_elasticity": self.advantage_dilution_elasticity,
        }
        for name, value in finite_values.items():
            if not isfinite(value):
                raise ValueError(f"{name} must be finite")
        nonnegative = {
            "shared_fixed_cost": self.shared_fixed_cost,
            "cross_node_learning": self.cross_node_learning,
            "integration_cost_scale": self.integration_cost_scale,
            "organization_cost_scale": self.organization_cost_scale,
            "advantage_dilution_elasticity": self.advantage_dilution_elasticity,
        }
        for name, value in nonnegative.items():
            if value < 0:
                raise ValueError(f"{name} cannot be negative")
        if self.learning_saturation <= 0:
            raise ValueError("learning_saturation must be positive")
        if not 0 < self.integration_cost_elasticity <= 1:
            raise ValueError(
                "integration_cost_elasticity must be greater than zero and at most one"
            )
        if self.organization_cost_elasticity < 1:
            raise ValueError("organization_cost_elasticity must be at least one")
        if isinstance(self.max_firm_size, bool) or not isinstance(
            self.max_firm_size, int
        ):
            raise ValueError("max_firm_size must be an integer")
        if self.max_firm_size < 1:
            raise ValueError("max_firm_size must be at least one")


@dataclass(frozen=True)
class FirmSizePoint:
    """Value produced by one candidate firm size."""

    size: int
    learning_fraction: float
    integration_cost_per_node: float
    organization_cost_per_node: float
    scale_component_per_node: float
    per_node_surplus: float
    total_surplus: float


@dataclass(frozen=True)
class FirmSizeOutcome:
    """Free-formation equilibrium and the underlying conditional size target.

    ``continuous_target_size`` is the zeta = 0 benchmark and is reported as
    NaN when ``advantage_dilution_elasticity`` is positive, because per-node
    surplus need not be single-peaked in continuous size under dilution.
    """

    regime: FirmSizeRegime
    integrates: bool
    equilibrium_firm_size: int
    conditional_target_size: int
    co_maximizing_sizes: tuple[int, ...]
    continuous_target_size: float
    integration_threshold: float
    internalization_advantage: float
    per_node_surplus_at_target: float
    total_surplus_at_target: float
    industry_boundary_binding: bool
    profile: tuple[FirmSizePoint, ...]


@dataclass(frozen=True)
class InternalizationBridgeOutcome:
    """A transparent bridge from the bilateral game to the scale model.

    Ownership is removed from the bilateral outside-option set, and the best
    resulting owner payoff is compared with full internal operation before any
    scale-specific costs. The difference is a private willingness to pay for
    internalization, not a social-welfare measure.
    """

    internalization_advantage: float
    gross_internal_owner_value: float
    best_modular_owner_value: float
    modular_regime: HiddenReuseRegime
    modular_agreement: bool
    modular_disclosure: float
    modular_reuse: float
    applied_context_payment_cap: Optional[float]


def _validate_size(size: int, primitives: FirmSizePrimitives) -> None:
    primitives.validate()
    if isinstance(size, bool) or not isinstance(size, int):
        raise ValueError("size must be an integer")
    if not 1 <= size <= primitives.max_firm_size:
        raise ValueError("size must lie between one and max_firm_size")


def learning_fraction(size: int, primitives: FirmSizePrimitives) -> float:
    """Fraction of the cross-node learning ceiling reached at firm size n."""

    _validate_size(size, primitives)
    return (size - 1.0) / (primitives.learning_saturation + size - 1.0)


def scale_component_per_node(size: int, primitives: FirmSizePrimitives) -> float:
    """Per-node surplus excluding the additive internalization advantage."""

    _validate_size(size, primitives)
    return (
        -primitives.shared_fixed_cost / size
        + primitives.cross_node_learning * learning_fraction(size, primitives)
        - primitives.integration_cost_scale
        * size ** (primitives.integration_cost_elasticity - 1.0)
        - primitives.organization_cost_scale
        * size**primitives.organization_cost_elasticity
    )


def advantage_per_node(size: int, primitives: FirmSizePrimitives) -> float:
    """Per-asset internalization advantage at firm size n: A * n**(-zeta).

    With the default ``advantage_dilution_elasticity`` of zero this is exactly
    ``internalization_advantage`` at every size.
    """

    _validate_size(size, primitives)
    return primitives.internalization_advantage * float(size) ** (
        -primitives.advantage_dilution_elasticity
    )


def per_node_surplus(size: int, primitives: FirmSizePrimitives) -> float:
    """Incremental private surplus per owned context-generating asset."""

    return advantage_per_node(size, primitives) + scale_component_per_node(
        size, primitives
    )


def firm_surplus(size: int, primitives: FirmSizePrimitives) -> float:
    """Total incremental private surplus created by a firm of size n."""

    return size * per_node_surplus(size, primitives)


def per_node_surplus_derivative(size: float, primitives: FirmSizePrimitives) -> float:
    """Derivative of continuous per-node surplus with respect to firm size.

    Includes the dilution term ``-zeta A n**(-zeta - 1)``, which vanishes when
    ``advantage_dilution_elasticity`` is zero.
    """

    primitives.validate()
    n = float(size)
    if not isfinite(n) or not 1 <= n <= primitives.max_firm_size:
        raise ValueError("continuous size must lie between one and max_firm_size")
    denominator = primitives.learning_saturation + n - 1.0
    return (
        primitives.shared_fixed_cost / (n * n)
        + primitives.cross_node_learning
        * primitives.learning_saturation
        / (denominator * denominator)
        + primitives.integration_cost_scale
        * (1.0 - primitives.integration_cost_elasticity)
        * n ** (primitives.integration_cost_elasticity - 2.0)
        - primitives.organization_cost_scale
        * primitives.organization_cost_elasticity
        * n ** (primitives.organization_cost_elasticity - 1.0)
        - primitives.advantage_dilution_elasticity
        * primitives.internalization_advantage
        * n ** (-primitives.advantage_dilution_elasticity - 1.0)
    )


def continuous_target_size(
    primitives: FirmSizePrimitives, *, tolerance: float = 1e-12
) -> float:
    """Solve the unique continuous target size by monotone bisection.

    Only defined for ``advantage_dilution_elasticity == 0``. With dilution the
    advantage term ``A n**(-zeta)`` is convex in continuous size, per-node
    surplus need not be single-peaked, and a bisection on the first-order
    condition is not guaranteed to locate the global optimum.
    """

    primitives.validate()
    if primitives.advantage_dilution_elasticity != 0.0:
        raise ValueError(
            "continuous_target_size requires advantage_dilution_elasticity == 0;"
            " per-node surplus need not be single-peaked under dilution"
        )
    lower = 1.0
    upper = float(primitives.max_firm_size)
    if per_node_surplus_derivative(lower, primitives) <= 0:
        return lower
    if per_node_surplus_derivative(upper, primitives) >= 0:
        return upper
    while upper - lower > tolerance:
        midpoint = 0.5 * (lower + upper)
        if per_node_surplus_derivative(midpoint, primitives) > 0:
            lower = midpoint
        else:
            upper = midpoint
    return 0.5 * (lower + upper)


def firm_size_profile(primitives: FirmSizePrimitives) -> tuple[FirmSizePoint, ...]:
    """Evaluate all feasible integer sizes."""

    primitives.validate()
    points = []
    for size in range(1, primitives.max_firm_size + 1):
        fraction = learning_fraction(size, primitives)
        scale_component = scale_component_per_node(size, primitives)
        per_node = advantage_per_node(size, primitives) + scale_component
        points.append(
            FirmSizePoint(
                size=size,
                learning_fraction=fraction,
                integration_cost_per_node=(
                    primitives.integration_cost_scale
                    * size ** (primitives.integration_cost_elasticity - 1.0)
                ),
                organization_cost_per_node=(
                    primitives.organization_cost_scale
                    * size**primitives.organization_cost_elasticity
                ),
                scale_component_per_node=scale_component,
                per_node_surplus=per_node,
                total_surplus=size * per_node,
            )
        )
    return tuple(points)


def solve_firm_size(
    primitives: FirmSizePrimitives, *, tolerance: float = 1e-10
) -> FirmSizeOutcome:
    """Solve the homogeneous free-formation firm-size equilibrium.

    Firms are ranked by incremental surplus per node. In a replica economy in
    which the selected size divides the population, equal-size firms paying
    that per-node surplus are in the transferable-utility core: no alternative
    coalition can offer every member more. Ties are reported in full and the
    smallest co-maximizer is selected to avoid imposing needless concentration.

    The ranking key is per-node surplus net of the size-independent part of
    the advantage, ``A (n**(-zeta) - 1) + h(n)``. With ``zeta = 0`` this is
    exactly the A-free scale component, so the conditional target does not
    depend on ``A``. With ``zeta > 0`` the advantage enters the ranking and
    the entry and size margins interact. The reported ``integration_threshold``
    generalizes accordingly to ``min_n(-h(n) n**zeta)``: the firm integrates
    if and only if ``A`` exceeds it.
    """

    primitives.validate()
    profile = firm_size_profile(primitives)
    zeta = primitives.advantage_dilution_elasticity
    advantage = primitives.internalization_advantage
    ranking = tuple(
        advantage * (float(point.size) ** -zeta - 1.0) + point.scale_component_per_node
        for point in profile
    )
    maximum_ranking_value = max(ranking)
    co_maximizers = tuple(
        point.size
        for point, value in zip(profile, ranking)
        if abs(value - maximum_ranking_value) <= tolerance
    )
    target_size = min(co_maximizers)
    target = profile[target_size - 1]
    threshold = min(
        -point.scale_component_per_node * float(point.size) ** zeta for point in profile
    )
    integrates = target.per_node_surplus > tolerance

    if not integrates:
        regime = FirmSizeRegime.MODULAR
        equilibrium_size = 0
    elif target_size == 1:
        regime = FirmSizeRegime.STANDALONE
        equilibrium_size = 1
    elif target_size == primitives.max_firm_size:
        regime = FirmSizeRegime.BOUNDARY
        equilibrium_size = target_size
    else:
        regime = FirmSizeRegime.ROLLUP
        equilibrium_size = target_size

    continuous_size = (
        continuous_target_size(primitives)
        if primitives.advantage_dilution_elasticity == 0.0
        else float("nan")
    )
    boundary_binding = (
        target_size == primitives.max_firm_size
        and per_node_surplus_derivative(float(primitives.max_firm_size), primitives)
        >= -tolerance
    )
    return FirmSizeOutcome(
        regime=regime,
        integrates=integrates,
        equilibrium_firm_size=equilibrium_size,
        conditional_target_size=target_size,
        co_maximizing_sizes=co_maximizers,
        continuous_target_size=continuous_size,
        integration_threshold=threshold,
        internalization_advantage=primitives.internalization_advantage,
        per_node_surplus_at_target=target.per_node_surplus,
        total_surplus_at_target=target.total_surplus,
        industry_boundary_binding=boundary_binding,
        profile=profile,
    )


def derive_owner_internalization_advantage(
    hidden_primitives: HiddenReusePrimitives,
    pricing_primitives: Optional[PledgeabilityPrimitives] = None,
    *,
    disclosure_grid_size: int = 401,
) -> InternalizationBridgeOutcome:
    """Map one bilateral scenario into the scale model's per-node advantage.

    The counterfactual makes ownership unavailable to the context owner while
    preserving renewal, multi-provider use, withholding, and exit. The owner's
    best bilateral payoff is subtracted from the value of full internal use
    before integration costs. This statistic includes rent capture as well as
    productive gains and should therefore be called a *private* advantage.
    """

    hidden_primitives.validate()
    gross_internal_value = current_benefit(1.0, hidden_primitives) + (
        hidden_primitives.discount_factor * hidden_primitives.future_integration_value
    )
    blocked_cost = max(
        1_000_000.0,
        gross_internal_value
        + hidden_primitives.future_multihome_value
        + hidden_primitives.provider_initial_outside_option
        + 1.0,
    )
    modular_primitives = replace(
        hidden_primitives,
        integration_cost=blocked_cost,
    )
    applied_cap: Optional[float] = None
    if pricing_primitives is None:
        governance = solve_hidden_reuse(
            modular_primitives,
            disclosure_grid_size=disclosure_grid_size,
        )
    else:
        priced = solve_with_pledgeability(
            modular_primitives,
            pricing_primitives,
            disclosure_grid_size=disclosure_grid_size,
        )
        governance = priced.governance
        applied_cap = priced.applied_context_payment_cap

    return InternalizationBridgeOutcome(
        internalization_advantage=(gross_internal_value - governance.owner_payoff),
        gross_internal_owner_value=gross_internal_value,
        best_modular_owner_value=governance.owner_payoff,
        modular_regime=governance.regime,
        modular_agreement=governance.agreement,
        modular_disclosure=governance.disclosure,
        modular_reuse=governance.reuse,
        applied_context_payment_cap=applied_cap,
    )
