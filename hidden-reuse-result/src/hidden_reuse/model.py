"""Two-period contracting when an AI provider can secretly reuse context.

Period one contracts specify disclosure, monitoring, and a transfer.  After the
contract is signed, the provider privately decides whether to reuse all
disclosed context.  Reuse changes period-two capability and outside options.
The context owner then renews, multi-homes, integrates, or exits.

The model intentionally keeps hidden reuse binary.  This makes the deterrence
condition and the resulting governance phase map auditable before introducing a
larger stochastic industry game.
"""

from dataclasses import dataclass
from enum import Enum
from math import log
from typing import Optional


class HiddenReuseRegime(str, Enum):
    """Governance regimes generated by the two-period game."""

    SECURE_MODULAR = "secure_modularity"
    PRICED_REUSE = "priced_reuse"
    WITHHOLDING = "strategic_withholding"
    OWNERSHIP = "ownership"

    # Backward-compatible name used by the exploratory implementation.
    LEAKY_MODULAR = PRICED_REUSE


class PeriodTwoRoute(str, Enum):
    """The context owner's period-two operating choice."""

    RENEW = "renew"
    MULTIHOME = "multi_home"
    INTEGRATE = "integrate"
    EXIT = "exit"


@dataclass(frozen=True)
class HiddenReusePrimitives:
    """Primitives of the two-period hidden-reuse game.

    ``enforcement_capacity`` is the maximum expected sanction per unit of
    secretly reused context under full monitoring.  ``context_payment_cap`` is
    the maximum explicit payment the provider can make to the owner per unit of
    disclosed context; it captures limited liability, non-pledgeability, or a
    prohibition on negative service prices.
    """

    current_marginal_value_at_full: float = 0.55
    current_curvature: float = 0.45
    discount_factor: float = 0.90

    future_renewal_surplus: float = 0.80
    future_multihome_value: float = 0.42
    future_integration_value: float = 0.62
    integration_cost: float = 0.70

    owner_reuse_loss: float = 0.75
    incumbent_capability_gain: float = 0.25
    provider_outside_base: float = 0.08
    provider_outside_gain: float = 0.45
    reuse_cost: float = 0.02

    enforcement_capacity: float = 0.45
    monitoring_cost: float = 0.25
    context_payment_cap: float = 0.00

    period_one_owner_bargaining_weight: float = 0.50
    period_two_owner_bargaining_weight: float = 0.50
    provider_initial_outside_option: float = 0.05

    def validate(self) -> None:
        if self.current_marginal_value_at_full <= 0:
            raise ValueError("current_marginal_value_at_full must be positive")
        if self.current_curvature <= 0:
            raise ValueError("current_curvature must be positive")
        if not 0 <= self.discount_factor <= 1:
            raise ValueError("discount_factor must lie in [0, 1]")
        nonnegative = {
            "future_renewal_surplus": self.future_renewal_surplus,
            "future_multihome_value": self.future_multihome_value,
            "future_integration_value": self.future_integration_value,
            "integration_cost": self.integration_cost,
            "owner_reuse_loss": self.owner_reuse_loss,
            "incumbent_capability_gain": self.incumbent_capability_gain,
            "provider_outside_base": self.provider_outside_base,
            "provider_outside_gain": self.provider_outside_gain,
            "reuse_cost": self.reuse_cost,
            "enforcement_capacity": self.enforcement_capacity,
            "monitoring_cost": self.monitoring_cost,
            "context_payment_cap": self.context_payment_cap,
            "provider_initial_outside_option": self.provider_initial_outside_option,
        }
        for name, value in nonnegative.items():
            if value < 0:
                raise ValueError(f"{name} cannot be negative")
        if not 0 <= self.period_one_owner_bargaining_weight <= 1:
            raise ValueError("period_one_owner_bargaining_weight must lie in [0, 1]")
        if not 0 <= self.period_two_owner_bargaining_weight <= 1:
            raise ValueError("period_two_owner_bargaining_weight must lie in [0, 1]")


@dataclass(frozen=True)
class PeriodTwoOutcome:
    """Continuation equilibrium after the provider's reuse decision."""

    route: PeriodTwoRoute
    reuse: float
    owner_payoff: float
    provider_payoff: float
    total_payoff: float
    owner_operating_outside: float
    provider_outside: float
    renewal_surplus: float
    bargaining_surplus: float


@dataclass(frozen=True)
class HiddenReuseOutcome:
    """Equilibrium contract and governance choice across both periods."""

    regime: HiddenReuseRegime
    agreement: bool
    disclosure: float
    monitoring: float
    reuse: float
    transfer_to_provider: float
    owner_payoff: float
    provider_payoff: float
    total_payoff: float
    owner_initial_outside: float
    integration_value: float
    waiting_value: float
    provider_reuse_gain_before_enforcement: float
    required_monitoring_to_deter: float
    payment_cap_binding: bool
    continuation: Optional[PeriodTwoOutcome]


@dataclass(frozen=True)
class _ContractCandidate:
    disclosure: float
    monitoring: float
    reuse: float
    transfer: float
    owner_payoff: float
    provider_payoff: float
    total_payoff: float
    log_nash_score: float
    provider_reuse_gain: float
    required_monitoring: float
    payment_cap_binding: bool
    continuation: PeriodTwoOutcome


def current_benefit(disclosure: float, primitives: HiddenReusePrimitives) -> float:
    """Period-one benefit from applying context to the current task."""

    s = float(disclosure)
    return (
        primitives.current_marginal_value_at_full + primitives.current_curvature
    ) * s - 0.5 * primitives.current_curvature * s * s


def solve_period_two(
    reuse: float, primitives: HiddenReusePrimitives, tolerance: float = 1e-12
) -> PeriodTwoOutcome:
    """Solve renewal bargaining and the owner's outside route after reuse.

    The owner's loss of exclusivity is sunk by period two and therefore does not
    affect the choice among operating routes.  It is subtracted after the route
    and bargaining outcome are determined.
    """

    primitives.validate()
    r = min(1.0, max(0.0, float(reuse)))
    outside_values = {
        PeriodTwoRoute.EXIT: 0.0,
        PeriodTwoRoute.MULTIHOME: primitives.future_multihome_value,
        PeriodTwoRoute.INTEGRATE: (
            primitives.future_integration_value - primitives.integration_cost
        ),
    }
    outside_route = max(outside_values, key=outside_values.get)
    owner_operating_outside = outside_values[outside_route]
    provider_outside = (
        primitives.provider_outside_base + primitives.provider_outside_gain * r
    )
    renewal_surplus = (
        primitives.future_renewal_surplus + primitives.incumbent_capability_gain * r
    )
    bargaining_surplus = renewal_surplus - owner_operating_outside - provider_outside

    if bargaining_surplus >= -tolerance:
        surplus = max(0.0, bargaining_surplus)
        owner_operating = (
            owner_operating_outside
            + primitives.period_two_owner_bargaining_weight * surplus
        )
        provider_payoff = (
            provider_outside
            + (1.0 - primitives.period_two_owner_bargaining_weight) * surplus
        )
        route = PeriodTwoRoute.RENEW
    else:
        surplus = 0.0
        owner_operating = owner_operating_outside
        provider_payoff = provider_outside
        route = outside_route

    owner_payoff = owner_operating - primitives.owner_reuse_loss * r
    return PeriodTwoOutcome(
        route=route,
        reuse=r,
        owner_payoff=owner_payoff,
        provider_payoff=provider_payoff,
        total_payoff=owner_payoff + provider_payoff,
        owner_operating_outside=owner_operating_outside,
        provider_outside=provider_outside,
        renewal_surplus=renewal_surplus,
        bargaining_surplus=surplus,
    )


def initial_outside_options(
    primitives: HiddenReusePrimitives,
) -> tuple[float, float, float]:
    """Return owner disagreement, immediate integration, and waiting values."""

    full_current = current_benefit(1.0, primitives)
    integration_value = (
        full_current
        + primitives.discount_factor * primitives.future_integration_value
        - primitives.integration_cost
    )
    waiting_value = primitives.discount_factor * max(
        0.0,
        primitives.future_multihome_value,
        primitives.future_integration_value - primitives.integration_cost,
    )
    return max(0.0, integration_value, waiting_value), integration_value, waiting_value


def provider_reuse_gain(disclosure: float, primitives: HiddenReusePrimitives) -> float:
    """Provider's continuation gain from secretly reusing disclosed context."""

    s = min(1.0, max(0.0, float(disclosure)))
    if s == 0:
        return 0.0
    no_reuse = solve_period_two(0.0, primitives)
    reuse = solve_period_two(s, primitives)
    return (
        primitives.discount_factor * (reuse.provider_payoff - no_reuse.provider_payoff)
        - primitives.reuse_cost * s
    )


def deterrence_threshold(disclosure: float, primitives: HiddenReusePrimitives) -> float:
    """Minimum enforcement capacity that can deter reuse at full monitoring.

    For positive disclosure s, no reuse is implementable exactly when
    enforcement capacity is at least provider_reuse_gain(s) / s. A
    non-positive private reuse gain requires no enforcement.
    """

    s = min(1.0, max(0.0, float(disclosure)))
    if s == 0:
        return 0.0
    return max(0.0, provider_reuse_gain(s, primitives) / s)


def required_monitoring_to_deter(
    disclosure: float, primitives: HiddenReusePrimitives
) -> float:
    """Minimum monitoring intensity needed to make no reuse optimal."""

    s = min(1.0, max(0.0, float(disclosure)))
    threshold = deterrence_threshold(s, primitives)
    if threshold == 0:
        return 0.0
    if primitives.enforcement_capacity == 0:
        return float("inf")
    return threshold / primitives.enforcement_capacity


def _best_transfer(
    owner_pretransfer: float,
    provider_pretransfer: float,
    disclosure: float,
    owner_outside: float,
    primitives: HiddenReusePrimitives,
    tolerance: float,
) -> Optional[tuple[float, float, float, float, bool]]:
    """Solve Nash bargaining over the transfer subject to the payment cap."""

    provider_outside = primitives.provider_initial_outside_option
    lower = max(
        provider_outside - provider_pretransfer,
        -primitives.context_payment_cap * disclosure,
    )
    upper = owner_pretransfer - owner_outside
    if lower > upper + tolerance:
        return None

    total_gain = (
        owner_pretransfer + provider_pretransfer - owner_outside - provider_outside
    )
    if total_gain <= tolerance:
        return None

    beta = primitives.period_one_owner_bargaining_weight
    desired_owner = owner_outside + beta * total_gain
    unconstrained_transfer = owner_pretransfer - desired_owner
    transfer = min(upper, max(lower, unconstrained_transfer))
    owner_payoff = owner_pretransfer - transfer
    provider_payoff = provider_pretransfer + transfer
    owner_gain = owner_payoff - owner_outside
    provider_gain = provider_payoff - provider_outside
    if owner_gain <= tolerance or provider_gain <= tolerance:
        return None

    if beta <= tolerance:
        score = log(provider_gain)
    elif beta >= 1.0 - tolerance:
        score = log(owner_gain)
    else:
        score = beta * log(owner_gain) + (1.0 - beta) * log(provider_gain)
    cap_binding = abs(transfer + primitives.context_payment_cap * disclosure) <= 1e-8
    return transfer, owner_payoff, provider_payoff, score, cap_binding


def _candidate(
    disclosure: float,
    monitoring: float,
    reuse: float,
    owner_outside: float,
    primitives: HiddenReusePrimitives,
    tolerance: float,
) -> Optional[_ContractCandidate]:
    continuation = solve_period_two(reuse, primitives)
    monitor_cost = (
        0.5 * primitives.monitoring_cost * monitoring * monitoring * disclosure
    )
    owner_pretransfer = current_benefit(disclosure, primitives) + (
        primitives.discount_factor * continuation.owner_payoff
    )
    provider_pretransfer = (
        primitives.discount_factor * continuation.provider_payoff
        - primitives.reuse_cost * reuse
        - monitor_cost
    )
    transfer_result = _best_transfer(
        owner_pretransfer,
        provider_pretransfer,
        disclosure,
        owner_outside,
        primitives,
        tolerance,
    )
    if transfer_result is None:
        return None
    transfer, owner_payoff, provider_payoff, score, cap_binding = transfer_result
    return _ContractCandidate(
        disclosure=disclosure,
        monitoring=monitoring,
        reuse=reuse,
        transfer=transfer,
        owner_payoff=owner_payoff,
        provider_payoff=provider_payoff,
        total_payoff=owner_payoff + provider_payoff,
        log_nash_score=score,
        provider_reuse_gain=provider_reuse_gain(disclosure, primitives),
        required_monitoring=required_monitoring_to_deter(disclosure, primitives),
        payment_cap_binding=cap_binding,
        continuation=continuation,
    )


def solve_hidden_reuse(
    primitives: HiddenReusePrimitives,
    *,
    disclosure_grid_size: int = 201,
    tolerance: float = 1e-10,
) -> HiddenReuseOutcome:
    """Solve the two-period hidden-reuse contracting game by backward induction.

    For each disclosure level, the contract either tolerates reuse with zero
    monitoring or installs exactly enough monitoring to deter it.  Any positive
    but non-deterring monitoring is strictly wasteful because sanctions are paid
    to a third party rather than transferred to the context owner.
    """

    primitives.validate()
    if disclosure_grid_size < 2:
        raise ValueError("disclosure_grid_size must be at least 2")

    owner_outside, integration_value, waiting_value = initial_outside_options(
        primitives
    )
    candidates: list[_ContractCandidate] = []

    for index in range(1, disclosure_grid_size + 1):
        disclosure = index / disclosure_grid_size
        gain = provider_reuse_gain(disclosure, primitives)

        if gain > tolerance:
            tolerated = _candidate(
                disclosure,
                0.0,
                disclosure,
                owner_outside,
                primitives,
                tolerance,
            )
            if tolerated is not None:
                candidates.append(tolerated)

            required = required_monitoring_to_deter(disclosure, primitives)
            if required <= 1.0 + tolerance:
                deterred = _candidate(
                    disclosure,
                    min(1.0, required),
                    0.0,
                    owner_outside,
                    primitives,
                    tolerance,
                )
                if deterred is not None:
                    candidates.append(deterred)
        else:
            naturally_secure = _candidate(
                disclosure,
                0.0,
                0.0,
                owner_outside,
                primitives,
                tolerance,
            )
            if naturally_secure is not None:
                candidates.append(naturally_secure)

    if not candidates:
        regime = (
            HiddenReuseRegime.OWNERSHIP
            if integration_value >= max(0.0, waiting_value) - tolerance
            and integration_value > tolerance
            else HiddenReuseRegime.WITHHOLDING
        )
        return HiddenReuseOutcome(
            regime=regime,
            agreement=False,
            disclosure=0.0,
            monitoring=0.0,
            reuse=0.0,
            transfer_to_provider=0.0,
            owner_payoff=owner_outside,
            provider_payoff=primitives.provider_initial_outside_option,
            total_payoff=(owner_outside + primitives.provider_initial_outside_option),
            owner_initial_outside=owner_outside,
            integration_value=integration_value,
            waiting_value=waiting_value,
            provider_reuse_gain_before_enforcement=0.0,
            required_monitoring_to_deter=0.0,
            payment_cap_binding=False,
            continuation=None,
        )

    best = max(
        candidates,
        key=lambda candidate: (
            candidate.log_nash_score,
            candidate.total_payoff,
            candidate.disclosure,
            -candidate.reuse,
        ),
    )
    # The disclosure grid always contains s = 1.0 exactly, so full-disclosure
    # regimes are labeled only at the top grid point.  A winner at any interior
    # point, however close to 1.0, is strategic withholding.
    if best.disclosure >= 1.0 - tolerance:
        regime = (
            HiddenReuseRegime.PRICED_REUSE
            if best.reuse > tolerance
            else HiddenReuseRegime.SECURE_MODULAR
        )
    else:
        regime = HiddenReuseRegime.WITHHOLDING

    return HiddenReuseOutcome(
        regime=regime,
        agreement=True,
        disclosure=best.disclosure,
        monitoring=best.monitoring,
        reuse=best.reuse,
        transfer_to_provider=best.transfer,
        owner_payoff=best.owner_payoff,
        provider_payoff=best.provider_payoff,
        total_payoff=best.total_payoff,
        owner_initial_outside=owner_outside,
        integration_value=integration_value,
        waiting_value=waiting_value,
        provider_reuse_gain_before_enforcement=best.provider_reuse_gain,
        required_monitoring_to_deter=best.required_monitoring,
        payment_cap_binding=best.payment_cap_binding,
        continuation=best.continuation,
    )
